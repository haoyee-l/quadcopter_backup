/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
void SysTick_Handler(void)
{
	MPU6050_Get_PRY(PRY, gyro, accel);
	PRYave[0] = (PRYave[0] + PRY[0])/2;
	PRYave[1] = (PRYave[1] + PRY[1])/2;
	PRYave[2] = (PRYave[2] + PRY[2])/2;
	UART1_ReportIMU(PRY[0],PRY[1],PRY[2],0);
	SystickNum++;
	if(SystickNum == 100)
	{
		
		PrintChar("WORKING...\n");
		LEDChangeState();
		SystickNum = 0;
	}
}

